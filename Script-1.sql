CREATE TABLE Endereco (
    CEP VARCHAR2(9) NOT NULL,
    Numero VARCHAR2(10) NOT NULL,
    Rua VARCHAR2(255) NOT NULL,
    Cidade VARCHAR2(100) NOT NULL,
    Estado CHAR(2) NOT NULL,
    CONSTRAINT pk_endereco PRIMARY KEY (CEP, Numero)
);

CREATE TABLE Banco (
    CNPJ_Banco VARCHAR2(18) NOT NULL,
    Nome_Banco VARCHAR2(100) NOT NULL,
    CONSTRAINT pk_banco PRIMARY KEY (CNPJ_Banco)
);

CREATE TABLE Pessoa (
    ID_Pessoa NUMBER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    Nome VARCHAR2(255) NOT NULL,
    Data_Nasc DATE NOT NULL,
    End_CEP VARCHAR2(9) NOT NULL,
    End_Numero VARCHAR2(10) NOT NULL,
    CONSTRAINT pk_pessoa PRIMARY KEY (ID_Pessoa),
    CONSTRAINT fk_pessoa_endereco FOREIGN KEY (End_CEP, End_Numero) REFERENCES Endereco(CEP, Numero)
);

CREATE TABLE Telefone (
    ID_Pessoa NUMBER NOT NULL,
    Numero_Telefone VARCHAR2(20) NOT NULL,
    CONSTRAINT pk_telefone PRIMARY KEY (ID_Pessoa, Numero_Telefone),
    CONSTRAINT fk_telefone_pessoa FOREIGN KEY (ID_Pessoa) REFERENCES Pessoa(ID_Pessoa) ON DELETE CASCADE
);

CREATE TABLE Pessoa_Fisica (
    ID_Pessoa NUMBER NOT NULL,
    CPF VARCHAR2(14) NOT NULL,
    CONSTRAINT pk_pessoa_fisica PRIMARY KEY (ID_Pessoa),
    CONSTRAINT fk_pf_pessoa FOREIGN KEY (ID_Pessoa) REFERENCES Pessoa(ID_Pessoa) ON DELETE CASCADE,
    CONSTRAINT uk_cpf UNIQUE (CPF)
);

CREATE TABLE Pessoa_Juridica (
    ID_Pessoa NUMBER NOT NULL,
    CNPJ VARCHAR2(18) NOT NULL,
    Razao_Social VARCHAR2(255) NOT NULL,
    CONSTRAINT pk_pessoa_juridica PRIMARY KEY (ID_Pessoa),
    CONSTRAINT fk_pj_pessoa FOREIGN KEY (ID_Pessoa) REFERENCES Pessoa(ID_Pessoa) ON DELETE CASCADE,
    CONSTRAINT uk_cnpj UNIQUE (CNPJ)
);

CREATE TABLE Representacao (
    ID_Representante NUMBER NOT NULL,
    ID_Representado NUMBER NOT NULL,
    CONSTRAINT pk_representacao PRIMARY KEY (ID_Representante, ID_Representado),
    CONSTRAINT fk_representante FOREIGN KEY (ID_Representante) REFERENCES Pessoa(ID_Pessoa),
    CONSTRAINT fk_representado FOREIGN KEY (ID_Representado) REFERENCES Pessoa(ID_Pessoa),
    CONSTRAINT chk_representacao_diferente CHECK (ID_Representante <> ID_Representado)
);

CREATE TABLE Conta (
    N_Conta VARCHAR2(20) NOT NULL,
    Agencia VARCHAR2(10) NOT NULL,
    CNPJ_Banco VARCHAR2(18) NOT NULL,
    Saldo NUMBER(15, 2) DEFAULT 0.00 NOT NULL,
    CONSTRAINT pk_conta PRIMARY KEY (N_Conta),
    CONSTRAINT fk_conta_banco FOREIGN KEY (CNPJ_Banco) REFERENCES Banco(CNPJ_Banco),
    CONSTRAINT chk_saldo_positivo CHECK (Saldo >= 0.00)
);

CREATE TABLE Titularidade (
    ID_Pessoa NUMBER NOT NULL,
    N_Conta VARCHAR2(20) NOT NULL,
    Tipo_Titular VARCHAR2(50) NOT NULL,
    Data_Adesao DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT pk_titularidade PRIMARY KEY (ID_Pessoa, N_Conta),
    CONSTRAINT fk_titular_pessoa FOREIGN KEY (ID_Pessoa) REFERENCES Pessoa(ID_Pessoa),
    CONSTRAINT fk_titular_conta FOREIGN KEY (N_Conta) REFERENCES Conta(N_Conta),
    CONSTRAINT chk_tipo_titular CHECK (Tipo_Titular IN ('PRINCIPAL', 'CONJUNTO', 'DEPENDENTE'))
);

CREATE TABLE Chave_Pix (
    Valor_Chave VARCHAR2(255) NOT NULL,
    N_Conta VARCHAR2(20) NOT NULL,
    Tipo_Chave VARCHAR2(20) NOT NULL,
    CONSTRAINT pk_chave_pix PRIMARY KEY (Valor_Chave),
    CONSTRAINT fk_chave_conta FOREIGN KEY (N_Conta) REFERENCES Conta(N_Conta),
    CONSTRAINT chk_tipo_chave CHECK (Tipo_Chave IN ('CPF', 'CNPJ', 'EMAIL', 'TELEFONE', 'ALEATORIA'))
);

CREATE TABLE Historico_Status (
    Data_Inicio DATE DEFAULT SYSDATE NOT NULL,
    ID_Pessoa NUMBER NOT NULL,
    N_Conta VARCHAR2(20) NOT NULL,
    Data_Fim DATE,
    Status VARCHAR2(50) NOT NULL,
    Motivo VARCHAR2(255),
    CONSTRAINT pk_historico PRIMARY KEY (Data_Inicio, ID_Pessoa, N_Conta),
    CONSTRAINT fk_historico_pessoa FOREIGN KEY (ID_Pessoa) REFERENCES Pessoa(ID_Pessoa),
    CONSTRAINT fk_historico_conta FOREIGN KEY (N_Conta) REFERENCES Conta(N_Conta),
    CONSTRAINT chk_status_conta CHECK (Status IN ('ATIVO', 'INATIVO', 'BLOQUEADO', 'ENCERRADO'))
);

CREATE TABLE Transacao (
    ID_Transacao NUMBER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    Data_Transacao DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT pk_transacao PRIMARY KEY (ID_Transacao)
);

CREATE TABLE Registro_Transacao (
    ID_Transacao NUMBER NOT NULL,
    N_Conta VARCHAR2(20) NOT NULL,
    Valor NUMBER(15, 2) NOT NULL,
    ID_Pagador NUMBER NOT NULL,
    ID_Receptor NUMBER NOT NULL,
    CONSTRAINT pk_registro_transacao PRIMARY KEY (ID_Transacao),
    CONSTRAINT fk_reg_transacao FOREIGN KEY (ID_Transacao) REFERENCES Transacao(ID_Transacao),
    CONSTRAINT fk_reg_conta FOREIGN KEY (N_Conta) REFERENCES Conta(N_Conta),
    CONSTRAINT fk_pagador FOREIGN KEY (ID_Pagador) REFERENCES Pessoa(ID_Pessoa),
    CONSTRAINT fk_receptor FOREIGN KEY (ID_Receptor) REFERENCES Pessoa(ID_Pessoa),
    CONSTRAINT chk_valor_transacao CHECK (Valor > 0.00)
);

CREATE TABLE Autorizacao_Transacao (
    N_Conta VARCHAR2(20) NOT NULL,
    ID_Pessoa NUMBER NOT NULL,
    ID_Transacao NUMBER NOT NULL,
    CONSTRAINT pk_autorizacao PRIMARY KEY (N_Conta, ID_Pessoa, ID_Transacao),
    CONSTRAINT fk_aut_conta FOREIGN KEY (N_Conta) REFERENCES Conta(N_Conta),
    CONSTRAINT fk_aut_pessoa FOREIGN KEY (ID_Pessoa) REFERENCES Pessoa(ID_Pessoa),
    CONSTRAINT fk_aut_transacao FOREIGN KEY (ID_Transacao) REFERENCES Transacao(ID_Transacao)
);

